package MailSystem.model;

public class ResultTable {
    private int total;
    private Object rows;

    public ResultTable() {}

    public ResultTable(int total, Object rows) {
        this.total = total;
        this.rows = rows;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public Object getRows() {
        return rows;
    }

    public void setRows(Object rows) {
        this.rows = rows;
    }
}
